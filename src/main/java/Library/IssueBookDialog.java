    private void btnIssueBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIssueBookActionPerformed
        // TODO add your handling code here:
        String memberID = txtMemberID.getText();
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String issuedDate = dateFormat.format(issuedDateField.getDate());
        String returnDate = dateFormat.format(returnDateField.getDate());
        
        try {
            pst = conn.prepareStatement("insert into issued_book(member_id, book_id, issued_date, return_date) values(?, ?, ?, ?)");
            pst.setString(1, memberID);
            pst.setInt(2, bookID);
            pst.setString(3, issuedDate);
            pst.setString(4, returnDate);
            int executed = pst.executeUpdate();
            
               if (executed == 1) {
                   JOptionPane.showMessageDialog(this, "Book issued successfuly");
                   
                   txtMemberID.setText("");
                   txtMemberID.requestFocus();
                   
                   dispose();
               } else {
                   JOptionPane.showMessageDialog(this, "Error issuing book");
               }
            
        } catch (SQLException ex) {
            Logger.getLogger(IssueBookDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnIssueBookActionPerformed

