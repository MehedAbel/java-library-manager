    public void loadPublishers() {
        try {
            pst = conn.prepareStatement("select * from publisher");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            int c = rsd.getColumnCount();
            
            DefaultTableModel d = (DefaultTableModel)tablePublishers.getModel();
            d.setRowCount(0);
            
            while (rs.next()) {
                Vector v = new Vector();
                
                for (int i = 1; i < c; i++) {
                    v.add(rs.getString("id"));
                    v.add(rs.getString("name"));
                    v.add(rs.getString("address"));
                    v.add(rs.getString("phone"));
                }
                
                d.addRow(v);
            }
        } catch (SQLException ex) {
            Logger.getLogger(PublishersPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

