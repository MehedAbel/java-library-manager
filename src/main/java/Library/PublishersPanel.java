    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel d = (DefaultTableModel)tablePublishers.getModel();
        int selectIndex = tablePublishers.getSelectedRow();
        int id = Integer.parseInt(d.getValueAt(selectIndex, 0).toString());

        try {
            pst = conn.prepareStatement("delete from publisher where id = ?");
            pst.setInt(1, id);
            int executed = pst.executeUpdate();

            if (executed == 1) {
                JOptionPane.showMessageDialog(this, "Publisher deleted");
                txtPublisher.setText("");
                txtAddress.setText("");
                txtPhone.setText("");
                txtPublisher.requestFocus();
                loadPublishers();
                btnAdd.setEnabled(true);
                btnEdit.setEnabled(false);
                btnDelete.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(this, "Error deleting publisher");
            }

        } catch (SQLException ex) {
            Logger.getLogger(PublishersPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        DefaultTableModel d = (DefaultTableModel)tablePublishers.getModel();
        int selectIndex = tablePublishers.getSelectedRow();
        int id = Integer.parseInt(d.getValueAt(selectIndex, 0).toString());

        String author_name = txtPublisher.getText();
        String address = txtAddress.getText();
        String phone = txtPhone.getText();

        try {
            pst = conn.prepareStatement("update publisher set name = ?, address = ?, phone = ? where id = ?");
            pst.setString(1, author_name);
            pst.setString(2, address);
            pst.setString(3, phone);
            pst.setInt(4, id);
            int executed = pst.executeUpdate();

            if (executed == 1) {
                JOptionPane.showMessageDialog(this, "Publisher updated");
                txtPublisher.setText("");
                txtAddress.setText("");
                txtPhone.setText("");
                txtPublisher.requestFocus();
                loadPublishers();
                btnAdd.setEnabled(true);
                btnEdit.setEnabled(false);
                btnDelete.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(this, "Error updating publisher");
            }

        } catch (SQLException ex) {
            Logger.getLogger(PublishersPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String name = txtPublisher.getText();
        String address = txtAddress.getText();
        String phone = txtPhone.getText();

        try {
            pst = conn.prepareStatement("insert into publisher(name, address, phone) values(?, ?, ?)");
            pst.setString(1, name);
            pst.setString(2, address);
            pst.setString(3, phone);

            int executed = pst.executeUpdate();

            if (executed == 1) {
                JOptionPane.showMessageDialog(this, "Publisher created");
                txtPublisher.setText("");
                txtAddress.setText("");
                txtPhone.setText("");
                txtPublisher.requestFocus();
                loadPublishers();
            } else {
                JOptionPane.showMessageDialog(this, "Error creating publisher");
            }

        } catch (SQLException ex) {
            Logger.getLogger(PublishersPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

